# String com aspas simples e duplas
string1 = 'Testando datatypes 1'
print("String com aspas simples")
print(string1)
print(type(string1))

string2 = "Testando datatypes 2"
print("String com aspas duplas")
print(string2)
print(type(string2))

# String com espaçamento em aspas triplas
string3 = '''Testando 
datatypes 
3'''
print("String com aspas triplas")
print(string3)
print(type(string3))

# String printando elementos/posição
# 0 corresponde a primeira posição
# -1 corresponde a última posição

print(string1[0])
print(string1[-1])
'''
 Strings são imutáveis
'''

#  Printando aspas com \' teste \'
string4 = 'Testando aspas 1'
